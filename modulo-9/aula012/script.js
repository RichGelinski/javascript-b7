let d = Math.abs(-78) // retorna o valor absoluto (positivo)
let e = Math.min(11, 43, -1, 8) //retorna o menor valor declarado
let f = Math.max(20, 30, 40, 100) // retorna o maior valor declarado
let g = Math.floor(Math.random() * 10 + 1) // retorna um valor aleatório entre 1 e 10

